//看起来是深拷贝，面对二维数组和对象时候还是浅拷贝，官方说这就是浅拷贝
//把内容展开
//拓展运算符方便了克隆
//拓展运算符是把对象或者数组内容以变量名的形式提取出来
let a = {a:1,b:2}
let b = {c:1,d:3}
let c = {...a,...b}
let d = {a:9,...c}
console.log(c);
console.log(d);
//合并对象
//也可以排除，写一个不要的属性键，需要的...other

let a1 = [1,2,3]
let b1 = [2,9]
let c1 = [99,...a1,100,...b1]
console.log(c1);
//合并数组

let test = [1,2,3]
function fn(a,b,c){
    console.log(a+b+c);
}
fn(...test)
//函数实参也能用拓展运算符数组代替
//总之拓展运算符需要跟随同类型，数组合数组，对象和对象
